	private void AddNewLaw()
	{
		SiliconLaw siliconLaw = new SiliconLaw();
		siliconLaw.Order = FixedPoint2.New(_laws.Count + 1);
		_laws.Add(siliconLaw);
		SetLaws(_laws);
	}

	public void SetLaws(List<SiliconLaw> sLaws)
	{
		_laws = sLaws;
		((Control)LawContainer).RemoveAllChildren();
		foreach (SiliconLaw item in sLaws.OrderBy((SiliconLaw l) => l.Order))
		{
			SiliconLawContainer siliconLawContainer = new SiliconLawContainer();
			siliconLawContainer.SetLaw(item);
			siliconLawContainer.MoveLawDown += MoveLawDown;
			siliconLawContainer.MoveLawUp += MoveLawUp;
			siliconLawContainer.DeleteAction += DeleteLaw;
			((Control)LawContainer).AddChild((Control)(object)siliconLawContainer);
		}
	}

	public void DeleteLaw(SiliconLaw law)
	{
		_laws.Remove(law);
		SetLaws(_laws);
	}

	public void MoveLawDown(SiliconLaw law)
	{
		if (_laws.Count != 0)
		{
			int num = _laws.IndexOf(law);
			if (num != -1)
			{
				_laws[num].Order += FixedPoint2.New(1);
				SetLaws(_laws);
			}
		}
	}

	public void MoveLawUp(SiliconLaw law)
	{
		if (_laws.Count != 0)
		{
			int num = _laws.IndexOf(law);
			if (num != -1)
			{
				_laws[num].Order += FixedPoint2.New(-1);
				SetLaws(_laws);
			}
		}
	}

	public List<SiliconLaw> GetLaws()
	{
		return _laws;
	}

