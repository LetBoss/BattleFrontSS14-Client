			}
			break;
		}
		case WarConditionStatus.WarReady:
		{
			TimeSpan timeSpan = _shuttleDisabledTime.Subtract(_gameTiming.CurTime);
			if (timeSpan > TimeSpan.Zero)
			{
				InfoLabel.Text = Loc.GetString("war-declarator-boost-timer", new(string, object)[1] { ("time", timeSpan.ToString("mm\\:ss")) });
			}
