      });
      ((Control) boxContainer1).Children.Add((Control) this.ResetButton);
      BoxContainer boxContainer2 = boxContainer1;
      ((BaseButton) this.ResetButton).OnPressed += (Action<BaseButton.ButtonEventArgs>) (args => parent._deferCommands.Add((Action) (() =>
      {
        parent._inputManager.ResetBindingsFor(function);
        parent._inputManager.SaveToUserData();
      })));
      this.AddChild((Control) boxContainer2);
    }
  }

  private sealed class BindButton : Control
  {
    private readonly KeyRebindTab _tab;
    public readonly KeyRebindTab.KeyControl KeyControl;
    public readonly Button Button;
    public IKeyBinding? Binding;

    public BindButton(KeyRebindTab tab, KeyRebindTab.KeyControl keyControl, string styleClass)
    {
      KeyRebindTab.BindButton button = this;
      this._tab = tab;
      this.KeyControl = keyControl;
      Button button1 = new Button();
      ((Control) button1).StyleClasses.Add(styleClass);
      this.Button = button1;
      this.UpdateText();
      this.AddChild((Control) this.Button);
      ((BaseButton) this.Button).OnPressed += (Action<BaseButton.ButtonEventArgs>) (args => tab.RebindButtonPressed(button));
      ((Control) this.Button).OnKeyBindDown += new Action<GUIBoundKeyEventArgs>(this.ButtonOnOnKeyBindDown);
      this.MinSize = new Vector2(200f, 0.0f);
    }

    protected virtual void EnteredTree()
    {
      base.EnteredTree();
      this._tab._inputManager.OnInputModeChanged += new Action(this.UpdateText);
    }

    protected virtual void ExitedTree()
    {
      base.ExitedTree();
      this._tab._inputManager.OnInputModeChanged -= new Action(this.UpdateText);
    }

    private void ButtonOnOnKeyBindDown(GUIBoundKeyEventArgs args)
    {
      if (!BoundKeyFunction.op_Equality(((BoundKeyEventArgs) args).Function, EngineKeyFunctions.UIRightClick))
        return;
      if (this.Binding != null)
        this._tab._deferCommands.Add((Action) (() =>
        {
          this._tab._inputManager.RemoveBinding(this.Binding, true);
          this._tab._inputManager.SaveToUserData();
        }));
      ((BoundKeyEventArgs) args).Handle();
    }

    public void UpdateText()
    {
      this.Button.Text = this.Binding?.GetKeyString() ?? Loc.GetString("ui-options-unbound");
    }
  }
