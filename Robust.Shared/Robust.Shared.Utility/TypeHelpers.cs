using System;
using System.Collections.Generic;
using System.Diagnostics.CodeAnalysis;
using System.Linq;
using System.Reflection;
using System.Runtime.CompilerServices;

namespace Robust.Shared.Utility;

public static class TypeHelpers
{
	public sealed class TypeInheritanceComparerImpl : IComparer<Type>
	{
		public int Compare(Type? x, Type? y)
		{
			if (x == null || y == null || x == y)
			{
				return 0;
			}
			if (x.IsAssignableFrom(y))
			{
				return -1;
			}
			if (y.IsAssignableFrom(x))
			{
				return 1;
			}
			return 0;
		}
	}

	internal static readonly IComparer<Type> TypeInheritanceComparer = new TypeInheritanceComparerImpl();

	public static IEnumerable<FieldInfo> GetAllFields(this Type t)
	{
		foreach (Type item in t.GetClassHierarchy())
		{
			FieldInfo[] fields = item.GetFields(BindingFlags.DeclaredOnly | BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic);
			for (int i = 0; i < fields.Length; i++)
			{
				yield return fields[i];
			}
		}
	}

	public static IEnumerable<PropertyInfo> GetAllProperties(this Type t)
	{
		return t.GetClassHierarchy().SelectMany((Type p) => p.GetProperties(BindingFlags.DeclaredOnly | BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic));
	}

	public static bool IsBasePropertyDefinition(this PropertyInfo propertyInfo)
	{
		MethodInfo[] accessors = propertyInfo.GetAccessors();
		foreach (MethodInfo methodInfo in accessors)
		{
			if (methodInfo.IsVirtual && methodInfo.GetBaseDefinition() != methodInfo)
			{
				return false;
			}
		}
		return true;
	}

	public static IEnumerable<Type> GetClassHierarchy(this Type t)
	{
		yield return t;
		while (t.BaseType != null)
		{
			t = t.BaseType;
			yield return t;
		}
	}

	public static IEnumerable<Type> GetAllNestedTypes(this Type t)
	{
		foreach (Type item in t.GetClassHierarchy())
		{
			Type[] nestedTypes = item.GetNestedTypes(BindingFlags.DeclaredOnly | BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic);
			for (int i = 0; i < nestedTypes.Length; i++)
			{
				yield return nestedTypes[i];
			}
		}
	}

	internal static IEnumerable<AbstractFieldInfo> GetAllPropertiesAndFields(this Type type)
	{
		foreach (FieldInfo allField in type.GetAllFields())
		{
			yield return new SpecificFieldInfo(allField);
		}
		foreach (PropertyInfo allProperty in type.GetAllProperties())
		{
			yield return new SpecificPropertyInfo(allProperty);
		}
	}

	public static bool TrySelectCommonType(Type type1, Type type2, [NotNullWhen(true)] out Type? commonType)
	{
		commonType = null;
		if (type1.IsAssignableFrom(type2))
		{
			commonType = type1;
		}
		else if (type2.IsAssignableFrom(type1))
		{
			commonType = type2;
		}
		return commonType != null;
	}

	internal static SpecificFieldInfo? GetBackingField(this Type type, string propertyName)
	{
		foreach (Type item in type.GetClassHierarchy())
		{
			FieldInfo field = item.GetField("<" + propertyName + ">k__BackingField", BindingFlags.Instance | BindingFlags.NonPublic);
			if (field != null)
			{
				return new SpecificFieldInfo(field);
			}
		}
		return null;
	}

	public static bool HasBackingField(this Type type, string propertyName)
	{
		return type.GetBackingField(propertyName) != null;
	}

	internal static bool TryGetBackingField(this Type type, string propertyName, [NotNullWhen(true)] out SpecificFieldInfo? field)
	{
		return (field = type.GetBackingField(propertyName)) != null;
	}

	public static bool IsBackingField(this MemberInfo memberInfo)
	{
		if (memberInfo.HasCustomAttribute<CompilerGeneratedAttribute>() && memberInfo.Name.StartsWith("<"))
		{
			return memberInfo.Name.EndsWith(">k__BackingField");
		}
		return false;
	}

	public static bool HasCustomAttribute<T>(this MemberInfo memberInfo) where T : Attribute
	{
		return memberInfo.GetCustomAttribute<T>() != null;
	}

	public static bool HasCustomAttribute<T>(this ParameterInfo memberInfo) where T : Attribute
	{
		return memberInfo.GetCustomAttribute<T>() != null;
	}

	public static bool TryGetCustomAttribute<T>(this MemberInfo memberInfo, [NotNullWhen(true)] out T? attribute) where T : Attribute
	{
		return (attribute = memberInfo.GetCustomAttribute<T>()) != null;
	}

	public static bool TryGetCustomAttribute(this MemberInfo memberInfo, Type type, [NotNullWhen(true)] out Attribute? attribute)
	{
		return (attribute = memberInfo.GetCustomAttribute(type)) != null;
	}

	internal static bool HasParameterlessConstructor(this Type type, BindingFlags flags = BindingFlags.Instance | BindingFlags.Public)
	{
		return type.GetConstructors(flags).Any((ConstructorInfo m) => m.GetParameters().Length == 0);
	}

	internal static bool IsAutogeneratedRecordMember(this AbstractFieldInfo info)
	{
		if (info.DeclaringType == null)
		{
			return false;
		}
		if (info.Name == "EqualityContract" && info.FieldType == typeof(Type))
		{
			return true;
		}
		return false;
	}
}
