	private void OnMainTabChanged(TabId? tabId)
	{
		if (tabId.HasValue)
		{
			if (tabId.Value == _profileTabId)
			{
				RequestProfileData();
				ShowProfileSubcategory(_currentProfileSubcategory);
			}
			else if (tabId.Value == _skinsTabId)
			{
				ShowSkinsSubcategory(_currentSkinsSubcategory);
			}
			else if (tabId.Value == _battlePassTabId)
			{
				ShowBattlePassSubcategory(_currentBattlePassSubcategory);
			}
		}
	}

