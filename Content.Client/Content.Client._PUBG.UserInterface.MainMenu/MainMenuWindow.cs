	private void ShowProfileSubcategory(ProfileSubcategory subcategory)
	{
		_currentProfileSubcategory = subcategory;
		if (_statsSubButton != null)
		{
			_statsSubButton.IsActive = false;
		}
		if (_sponsorsSubButton != null)
		{
			_sponsorsSubButton.IsActive = false;
		}
		switch (subcategory)
		{
		case ProfileSubcategory.Stats:
			if (_statsSubButton != null)
			{
				_statsSubButton.IsActive = true;
			}
			break;
		case ProfileSubcategory.Sponsors:
			if (_sponsorsSubButton != null)
			{
				_sponsorsSubButton.IsActive = true;
			}
			break;
		}
		LoadProfileTab();
	}

