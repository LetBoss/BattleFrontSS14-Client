	private void LoadProfileTab()
	{
		if (_profileContentContainer != null)
		{
			((Control)_profileContentContainer).RemoveAllChildren();
			if (_profileTab == null)
			{
				_profileTab = new ProfileTab();
				_profileTab.OnOpenSponsorDisplaySettingsRequested += OnProfileOpenSponsorDisplaySettingsRequested;
				_profileTab.UpdateSponsorData(_cachedSponsorPermissions, _cachedSponsorPermissionDetails, _cachedSponsorDisplayTier, _cachedSponsorActiveTiers, _cachedSponsorDisplayMode, _cachedSponsorPreferredTierKey);
				_profileTab.UpdateStatsData(_cachedTotalGames, _cachedWins, _cachedTotalKills, _cachedTotalDamage, _cachedTotalSurvivalTime, _cachedTotalCaseDropSkins, _cachedUnlockedCaseDropSkins, _cachedTotalEmotes, _cachedAvailableEmotes, _cachedTotalDeaths, _cachedReputation);
				_profileTab.UpdateLeaderboardData(_cachedLeaderboard, _cachedPlayerRank, _cachedPlayerRating);
				_profileTab.UpdateMatchHistoryData(_cachedMatchHistory);
			}
			_profileTab.LoadSubcategory(_currentProfileSubcategory);
			((Control)_profileContentContainer).AddChild((Control)(object)_profileTab);
		}
	}

	private void RequestProfileData()
	{
		if (!_profileDataRequested)
		{
			_profileDataRequested = true;
			SkinProfileRequestMessage skinProfileRequestMessage = new SkinProfileRequestMessage();
			_entityManager.RaisePredictiveEvent<SkinProfileRequestMessage>(skinProfileRequestMessage);
		}
	}

	private void LoadSkinsTab()
	{
		if (_skinsContentContainer == null)
		{
			return;
		}
		((Control)_skinsContentContainer).RemoveAllChildren();
		if (_skinsTab == null)
		{
			_skinsTab = new SkinsTab();
			_skinsTab.OnApply += delegate(Dictionary<string, string> outfit)
			{
				this.OnApplyOutfit?.Invoke(outfit);
			};
			_skinsTab.OnHeroCaseOpenRequested += OnHeroCaseOpenRequested;
			if (_cachedShopItems.Count > 0)
			{
				_skinsTab.UpdateShopItems(_cachedShopItems);
			}
		}
		_skinsTab.SetHeroCasePending(_cachedHeroCasePending);
		_skinsTab.SetHeroCaseError(_cachedHeroCaseErrorCode);
		_skinsTab.LoadSubcategory(_currentSkinsSubcategory);
		((Control)_skinsContentContainer).AddChild((Control)(object)_skinsTab);
	}

